# Write a program to: Sort the list, reverse the list and counting elements 
# in a list. 

# list creation
l = [20, 50, 30, 10, 40]

# Sorting list elements using sort()
l.sort()
print(l)

# Reversing list elements using reverse()
l.reverse()
print(l)

# Reverse Sorting
l.sort(reverse = True)
print(l)

# Counting List Elements
print("No.of Elements in a List: ", len(l))